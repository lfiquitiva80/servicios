     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data =  new  calendario($request->all());
        $data->save();
        Alert::success('', 'la fecha ha sido sido registrada con exito!')->persistent('Close');
        return redirect()->route('calendarioprefactura.index');
    }

